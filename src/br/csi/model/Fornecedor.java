package br.csi.model;

public class Fornecedor {
		
	private long codigo;
	private String razaoS;
	
	public long getCodigo() {
		return codigo;
	}
	public void setCodigo(long codigo) {
		this.codigo = codigo;
	}
	public String getRazaoS() {
		return razaoS;
	}
	public void setRazaoS(String razaoS) {
		this.razaoS = razaoS;
	}
}
